package Backend;

public class Pair {
    public int firstR;
    public int firstC;
    public int secondR;
    public int secondC;

    public Pair(int firstR, int firstC, int secondR, int secondC){
        this.firstR = firstR;
        this.firstC = firstC;
        this.secondR = secondR;
        this.secondC = secondC;
    }
}
